package com.JobDescriptionGenerator.view;

public class JobRequest {

	private String input_job_title;
	private String input_skills;
	private String input_company_name;
	private String input_location;
	
	public String getInput_job_title() {
		return input_job_title;
	}
	public void setInput_job_title(String input_job_title) {
		this.input_job_title = input_job_title;
	}
	public String getInput_skills() {
		return input_skills;
	}
	public void setInput_skills(String input_skills) {
		this.input_skills = input_skills;
	}
	public String getInput_company_name() {
		return input_company_name;
	}
	public void setInput_company_name(String input_company_name) {
		this.input_company_name = input_company_name;
	}
	public String getInput_location() {
		return input_location;
	}
	public void setInput_location(String input_location) {
		this.input_location = input_location;
	}
	
	
}
